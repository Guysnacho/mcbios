					<p>
						The single-cell analyses will allow researchers to uncover new and potentially
						unexpected biological discoveries relative to traditional profiling methods that assess
						bulk populations. This tutorial is to provide an overview of the laboratory and
						computational method involved in generating and analyzing single cell data. Participants
						will be introduced to the workflow of single cell analysis, the principles of single
						cell platforms, including Fluidigm C1/ Biomark, 10xGenomics, BD Rhapsody, and iCell8,
						and will focus on different solutions of 10xGenomics. The tutorial will introduce the
						main computational algorithms and tools for single cell data analysis, including
						demultiplexing sequence alignment, quality control, identifying clusters of cells and
						performing analyses to assign cell identity and function. We will also give an
						introduction of single-cell RNA sequencing in immunology and use Loupe browser to show
						how to work with single cell V(D)J seq data. There will be a hands-on case study on
						single cell RNAseq data analysis. Analyses will be performed by using Seurat in R
						interfaces.
					</p>
