																<a style="text-decoration: underline;" href="http://www.iscb.org/"
																	>International Society of Computational Biology</a
																>
																(ISCB). MCBIOS 2019 will be hosted by the University of Alabama at Birmingham
																School of Medicine and co-sponsored by various Academic and industrial
																entities related to genomics, information technology, and health sciences,
																and biopharmaceuticals.
															</p>
															<p>
																Attendees will share and discuss novel ideas on emerging topics
																related to genomics, bioinformatics, biocomputing, medical
																informatics, algorithmic/database development, applied statistics,
																bioengineering, data sciences, and their relevant precision medicine
																applications in disease and consumer health. We welcome researchers
																in all stages of career, from students, postdocs, junior faculty,
																and experienced researchers, to disseminate late-breaking
																interdisciplinary research work at this meeting.
