																The
																<strong
																	>16<sup>th</sup> Annual MidSouth Conference on Computational Biology
																	and Bioinformatics</strong
																>
																(<a style="text-decoration: underline;" href="/"
																	><strong>MCBIOS ’19</strong></a
																>) program committee solicits one-page abstracts related to the
																research theme of “<strong
																	>Informatics for Precision Medicine</strong
																>” for presentations (both oral and poster) to be held in March
																28-30 at the DoubleTree Hilton Hotel, Birmingham, AL.
															</p>
															<p>
																MCBIOS annual conference is a premier annual meeting for
																bioinformatics researchers, professionals, and trainees in the
																Middle and Southern areas of the United States. It has a major venue
																for informaticians to exchange late-breaking research results, learn
																technological trends, and network in the region. This annual event
																has been organized by the
																<a style="text-decoration: underline;" href="http://www.mcbios.org/"
																	>MCBIOS society</a
																>
